            'tax_id' => 'required|string|max:255',
            'category_id.*' => 'required|numeric|integer',
            'img' => 'string',
        ]);

        $newRestaurant = new Restaurant();
        $newRestaurant-> categories()->attach($data['category_id']);
        $newRestaurant->fill($data);
        $newRestaurant->save();
        
